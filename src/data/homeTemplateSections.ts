/** แถวเทมเพลตบนหน้า Home — query ไป Polotno template API */
export type HomeTemplateSection = {
  id: string
  title: string
  query: string
  /** default size=all */
  sizeQuery?: string
  /** จำนวนการ์ดสูงสุดในแถวแนวนอน */
  maxItems: number
  /** โหลดหลายหน้า API ล่วงหน้า */
  prefetchPages: number
  /** ปุ่ม "ดูทั้งหมด" เปิดโมดัลหมวดนี้ */
  modalGroupId?: string
  /** query สำรองเมื่อ API หมวดหลักว่าง */
  fallbackQueries?: string[]
  /** แสดงเฉพาะเทมเพลตคัดสรรใน repo (ไม่เรียก API) */
  curatedOnly?: boolean
  /** แหล่งเทมเพลต — default catalog (แคตตาล็อกใน repo) */
  templateSource?: 'catalog' | 'curated' | 'api' | 'local' | 'unsplash'
  /** ใช้กับ templateSource: unsplash */
  unsplashQuery?: string
}

